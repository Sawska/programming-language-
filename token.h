#ifndef TOKEN_H

#define TOKEN_H 



struct TOKEN
{
    enum OPERATORS {
    PLUS_OPERATOR ,
    MINUS_OPERATOR,
    MULTIPLY_OPERATOR,
    DIVIDE_OPERATOR,
    AND_OPERATOR,
    OR_OPERATOR,
    NOT_OPERATOR,
    BIT_AND_OPERATOR,
    BIT_OR_OPERATOR,
    BIT_NOT_OPERATOR,
    BIGGER_OPERATOR,
    SMALLER_OPERATOR,
    LEFT_SHIT_OPERATOR,
    RIGHT_SHIT_OPERATOR,
    EQUALS_OPERATOR,
    QUESTION_OPERATOR,
    INCREMENT_OPERATOR,
    COMPARE_OPERATOR,
    DECREMENT_OPERATOR,
};


enum TOKEN_CONCEPTS {
    NUMBER,
    OPEN_CIRCLE,
    CLOSE_CIRCLE,
    ERROR_TOKEN,
};



    TOKEN_CONCEPTS concept;
    OPERATORS op;
    std::string number;
};




#endif //TOKEN_H;